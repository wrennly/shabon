RAG_SIMILARITY_THRESHOLD = 0.5
"""
コサイン類似度の足切りライン(0.0〜1.0)

- 値: 0.7 → 類似度70%以下の記憶は「無関係」として除外
- 高すぎる(0.9+): 厳しすぎて何も引っかからない
- 低すぎる(0.5-): 無関係な記憶も混ざる
- 推奨: 0.65-0.75

【効果】
- 関連する話題がある時 → 過去の記憶を活用
- 全く新しい話題の時 → 何も持ってこない(ノイズゼロ)
"""

